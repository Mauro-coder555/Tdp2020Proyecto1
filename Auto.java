
public class Auto {
	
	// Atributos
	
	private String color;
	private int calidad;
	
	

	// Constructor

	public Auto(String color,int calidad) {
		this.color = color;
		this.calidad = calidad;
		
		
	}
	
	
	// Getters, Setters
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getCalidad() {
		return calidad;
	}
	public void setCalidad(int calidad) {
		this.calidad = calidad;
	}
	
	// Getters, Setters
	
	
	
	
	
}